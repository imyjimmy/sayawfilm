					<p>Although raised in Palawan, Jay Loyola was first introduced to the rich cultural tradition of Tagbanua tribe in 1996 on a dance field research with the Bayanihan Dance Company. Loyola formed a deep kinship with Palawan’s native people, and was adopted into the Tagbanua tribe in Napsan. There, he participated in sacred rituals, traditional weddings, healing dances, and cultural practices of the tribe. Concerned that Tagbanua youth were not learning or practicing the traditional art forms, Loyola established the all-indigenous Palawan Dance Theater. Supported by the Palawan Center for the Arts Foundation, Palawan Dance Theatre became a vehicle to preserve and present the traditional Palawan culture through innovative choreography and stage presentation. Dancers and musicians were trained not only in technique and performance, but also in research and documentation of tribal customs.</p>
