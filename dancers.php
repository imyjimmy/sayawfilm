          <p>Loyola toured internationally as a principal dancer of the Bayanihan Philippine National Folk Dance Company from 1989-98. Considered a protégé of choreographer Lucrecia Reyes- Urtula, who was awarded the prestigious Philippine’s National Artist for Folk Dance and was the founding dance director of the Bayanihan Dance Company, Jay served as the Company’s Junior Assistant Dance Director in 1994-96. He taught Philippine folk dance workshops in the Philippines and for international festivals and conferences such as Singapore Arts Festival; Kaliningrad International Dance Festival in Russia, Konnichiwa Festival in Tokyo, Macau Arts Festival in China and the Philippine International Dance Festival and World Folklore Festival under the auspices of Conseil International des Organisations de Festivals de Folklore et d’Arts Traditionnels in France, Germany, Italy and the Philippines.</p>
